#include <iostream>
using namespace std;

int main(void)
{
	int n, i, j;
	cin >> n;
	i = 0;
	while (i < n)
	{
		j = 0;
		while (j < n - i - 1)
		{
			cout << " ";
			j++;
		}
		j = 0;
		while (j < 2 * i + 1)
		{
			if (i == n - 1)
				cout << "*";
			else
			{
				if (j == 0 || j == 2 * i)
					cout << "*";
				else
					cout << " ";
				
			}
			
			j++;
		}
		cout << "\n";
		i++;
	}
}