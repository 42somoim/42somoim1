#include <iostream>

using namespace std;

int main(void)
{
	int n, i, j;
	cin >> n;
	i = 0;
	while (i < n)
	{
		j = 0;
		while (j < n - i - 1)
		{
			cout << " ";
			j++;
		}
		j = 0;
		while (j < i + 1)
		{
			cout << "* ";
			j++;
		}
		cout << "\n";
		i++;
	}
}