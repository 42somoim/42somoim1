#include <iostream>

using namespace std;

int main(void)
{
	int n, m;
	cin >> n >> m;

	if (m == 1 || n == 1)
	{
		cout << 1;
	}
	else if (m >= 6 && n >= 3)
	{
		cout << 4 + (m - 6);
	}
	else if (n == 2)
	{
		cout << ((m > 8) ? 4 : (m + 1) / 2);
	}
	else // n >= 4 && m >= 2 && m <= 5
	{
		cout << ((m > 4) ? 4 : m);
	}
}