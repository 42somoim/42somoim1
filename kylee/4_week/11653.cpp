#include <iostream>

using namespace std;

int main(void)
{
	int n, i;
	cin >> n;
	i = 2;
	while (n != 1)
	{
		if (n % i == 0)
		{
			n /= i;
			cout << i << "\n";
		}
		else
			i++;
	}
}